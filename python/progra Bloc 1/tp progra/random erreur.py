def randint(min_value: int, max_value : int) -> int : 
    """ Return a random integer in [min_value, max_Value].

    Parameters
    ----------

    min_value : minimum possible value (int)
    max_value : maximum possible value (int)

    returns
    -------

    number : random integer (int) """
    return randint(min_value, max_value)


